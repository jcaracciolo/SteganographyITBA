package ar.edu.itba.crypto.utils;

public class ConsoleArguments {

    private String embed;
    private String in;
    private String p;
    private String out;
    private String steg;
    private String a;
    private String m;
    private String pass;

    public ConsoleArguments(String embed, String in, String p, String out, String steg, String a, String m, String pass) {
        this.embed = embed;
        this.in = in;
        this.p = p;
        this.out = out;
        this.steg = steg;
        this.a = a;
        this.m = m;
        this.pass = pass;

    }


}
